
public class WhileLoop {

	public static void main(String[] args) {
		int i=1;
		//1....10 numbers
		while(i<=10) {
			System.out.println(i);
			i=i+1;//i++
		}
		
		//1...10 even numbers
		int b=2;
		while(b<=10){
			System.out.println(b);
			b=b+2; //b+=2
		}
		//odd numberes
		int a=1;
		while (a<=10) {
			System.out.println(a);
			a+=2;
		}
		
		//descending order
		int z=10;
		while(z>0) {
			System.out.println(z);
			z--;
		}
		
		

	}

}
