
public class ForLoop {

	public static void main(String[] args) {
		//1....10
		
		for(int i=1;i<=10; i++ ) {
			System.out.println(i);
	}
		
		//even  numbers
		
		for(int a=2; a<=10; a+=2) {
			System.out.println(a);
		}

		//decending order
		
		for(int b=10;b>0;b--) {
			System.out.println(b);
		}
}
}
